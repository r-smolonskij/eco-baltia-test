     * Store a newly created resource in storage.
     *
     * @param  \App\Http\Requests\StoreOrderRequest  $request
     * @return \Illuminate\Http\Response
     */
    public function store(StoreOrderRequest $request)
    {
        $request->validate([
            'bookId' => 'required',
        ]);
        $book = Book::find($request->bookId);
        $messageText = $book ? "Grāmata '$book->title' veiksmīgi nopirkta " : "Kaut kas nogāja greizi!";
        Order::create(array('book_id' => $request->bookId, "order_date" => new DateTime()  ));
        return redirect()->route('books')->with($book ? 'successMessage' : 'errorMessage', $messageText);
    }

